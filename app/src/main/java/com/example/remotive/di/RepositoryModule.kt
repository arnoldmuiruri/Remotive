package com.example.remotive.di

import com.example.remotive.databases.CareerDatabase
import com.example.remotive.network.RemotiveService
import com.example.remotive.repository.CareerRepositoryImpl
import dagger.Module
import dagger.Provides
import dagger.hilt.InstallIn
import dagger.hilt.components.SingletonComponent
import javax.inject.Singleton

@Module
@InstallIn(SingletonComponent::class)
object RepositoryModule {

    @Provides
    @Singleton
    fun provideRepository(
        remotiveService: RemotiveService,
        database: CareerDatabase
    ):CareerRepositoryImpl{
        return CareerRepositoryImpl(remotiveService,database)
    }
}